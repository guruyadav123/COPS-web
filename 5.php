stipulating the organizations' objectives, burgeoning policies and simultaneously outlining plans to achieve them, Adhyayan 
provides one with an opportunity to showcase analytical 
skills involving new dimensions of on-the-ground reality and take a leap into the real world of critical case studies. </p>
</div>
<div class="w3-col s12 m12 l4 w3-padding w3-text-white "> 
<h3 class="w3-text-black">Rachna </h3>
<p> Catering to the perpetual need to optimize the design and operating conditions of industrial
 operations, Rachna is an event focused on designing and optimization of an industrial production process
 considering both financial and environmental  
aspects in verticals of process design, process control, model development, process identification, and real-time optimization.</p>
