<h3 class="w3-text-black">Chem-E-Boat </h3>
<p>One of the brand new event of Osmoze, Chem-E-Boat an annual event which will be serving as an opportunity for 
chemical engineering undergraduates to utilize their knowledge and skills attained through engineering coursework and
 construct a small-scale boat powered by an unconventional source of chemical energy. 
Filled with thrill and competitiveness, Chem-E-Boat is going to transform your way of viewing an energy source. </p>
 </div> 
