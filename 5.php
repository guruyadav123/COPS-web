</div>
<div class="w3-col l4 m12 s12 w3-padding w3-text-white"> 
<h3 class="w3-text-green" >The Hosts </h3>
<p>Osmoze is hosted by the Department of Chemical Engineering and Technology, IIT (BHU), Varanasi: a Pioneer in the
 discipline with roots tracing back to almost a century. </p>
</div>
<div class="w3-col l4 m12 s12 w3-padding  w3-text-white">
<h3 class="w3-text-green">About the Society </h3>
<p> The Society of Chemical Engineers, lovingly called SoChem, is a student's organization under the patronage of 
the Department of Chemical Engineering, IIT (BHU), Varanasi. It acts as an 
interface between the students and the experienced faculty and administration of the department.</p>
 </div>
