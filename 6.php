<h3 class="w3-text-white w3-center"> KHAJJIAR(HIMACHAL PRADESH)</h3>
<p style="text-align:justify;">This beautiful place bears a very striking 
resemblance to Switzerland’s topography, the well known romanticizing location. Khajjiar 
is located at the foot of Dhauladkar 
, in the lap of Himalayas and is 2000 meters above sea level.
A beautiful panoramic view of the densely populated forests and the meadows and 
snowy peaks of Himalayas can be captured. Summer months are the best to visit this place
 as it wouldn’t be too cold and the hill station would be laden with beautiful flowers which makes
 it one of the most beautiful place in India. </p>
