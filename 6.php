<h3 class="w3-text-white w3-center"> MANALI(HIMACHAL PRADESH )</h3>
<p style="text-align:justify;">Manali is a resort town nestled in the mountains of the Indian state
 of Himachal Pradesh near the northern end of the Kullu Valley, at an altitude of 2,050 m (6,726 ft) in the Beas 
 River Valley. It is located in the Kullu district, about 270 km (168 mi) north of the state capital, Shimla, 309 km (192 miles) 
 northeast of Chandigarh and 544 km (338 miles) northeast of Delhi, the federal capital. The small town, with a population of 8,096,
 is the beginning of an ancient trade route to Ladakh and from there over the Karakoram Pass on to Yarkand and Khotan in the
 Tarim Basin. It is
 a popular tourist destination and serves as the gateway to Lahaul and Spiti district as well as Leh.
